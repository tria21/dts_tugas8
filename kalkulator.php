<?php
//function penjumlahan
function jumlah($a, $b) {
    return ($a + $b);
}

echo "Hasil Penjumlahan adalah ". jumlah(6,2) . "</br>";

//function pengurangan
function kurang($a, $b) {
    return ($a - $b);
}
echo "Hasil Pengurangan adalah ". kurang(6,2) . "</br>";

//function perkalian
function kali($a, $b) {
    return ($a * $b);
}
echo "Hasil Perkalian adalah ". kali(6,2) . "</br>";

//function pembagian
function bagi($a, $b){
    return ($a / $b);
}
echo "Hasil Pembagian adalah ". bagi(6,2) . "</br>";
?>